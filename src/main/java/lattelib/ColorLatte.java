    private int r, g, b;

    public ColorLatte(int r, int g, int b) {
        this.r=r;
        this.g=g;
        this.b=b;
    }

    public ColorLatte(String s) {
        try {
            this.r = Integer.parseInt(s.substring(1,3),16);
            this.g = Integer.parseInt(s.substring(3,5),16);
            this.b = Integer.parseInt(s.substring(5,7),16);
        } catch (Exception e) {
            this.r=0;
            this.g=0;
            this.b=0;
        }
    }

    public String toString() {
        return String.format("#%02x%02x%02x", this.getRed(), this.getGreen(), this.getBlue());
    }

    public int getRed() {
        return r;
    }
    public int getGreen() {
        return g;
    }
    public int getBlue() {
        return b;
    }
